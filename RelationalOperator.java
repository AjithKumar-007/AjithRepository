package day1;

public class RelationalOperator {
	public static void main (String[]args) {
		int sum1=10;
		int sum2=20;
		System.out.println(sum1<sum2);
		
		int sum3=5;
		int sum4=20;
		System.out.println(sum3<=sum4);
		
		int sum5=11;
		int sum6=22;
		System.out.println(sum5>sum6);
	
		
		int sum7=11;
		int sum8=22;
		System.out.println(sum7>=sum8);
		
		int sum9=11;
		int sum10=22;
		System.out.println(sum9==sum10);
		
		int sum11=11;
		int sum12=22;
		System.out.println(sum11!=sum12);

		
		
		
	}

}
